 // A+B, A-B, A*B, A/B(몫), A%B(나머지)를 출력
import java.util.Scanner;


public class BJTest10869{

	public static void main(String[] args){
		
		Scanner sc = new Scanner(System.in);
		
		int a, b;
		a = sc.nextInt();
		b = sc.nextInt();
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
	
	}
}