public class Java100_method_Exam001{
	public static void helloWorld(){
		// 입력 값, 반환 값 없음
		System.out.println("Hello World!");
	}
	
	public static void showMenu(){
		// 입력 값, 반환 값 없음
		System.out.println("showMenu() 메서드 호출");
	}
	public static void main(String[] args){
		helloWorld();
		showMenu();
	}
}