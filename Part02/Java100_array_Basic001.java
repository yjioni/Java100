public class Java100_array_Basic001 {
	public static void main(String[] args) {
		
		int[] sales_table = new int[100];
		System.out.println(sales_table);
		
		double sales_table2[] = new double[10];
		System.out.println(sales_table2);
		
		String[] sales_table3 = new String[5];
		System.out.println(sales_table3);
		
		sales_table[9] = 10;
		System.out.println(sales_table[9]);
		
	}
}